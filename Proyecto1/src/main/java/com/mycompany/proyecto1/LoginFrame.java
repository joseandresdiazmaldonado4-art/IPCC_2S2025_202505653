    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String usuario = jTextField1.getText().trim();
    String contrasena = String.valueOf(jPasswordField1.getPassword()).trim();

    if (usuario.equals("") || contrasena.equals("")) {
        javax.swing.JOptionPane.showMessageDialog(this, "Debe ingresar usuario y contrasena.");
        return;
    }

    Usuario usuarioValido = sistema.validarLogin(usuario, contrasena);

    if (usuarioValido == null) {
        javax.swing.JOptionPane.showMessageDialog(this, "Usuario o contrasena incorrectos.");
        return;
    }

    javax.swing.JOptionPane.showMessageDialog(this, "Login correcto de: " + usuarioValido.obtenerNombre());
    }//GEN-LAST:event_jButton1ActionPerformed

